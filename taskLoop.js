function naikSatu() {
    var start = 1;
    while (start <= 15) {
        console.log(start);
        start++;
    }
}
naikSatu();
console.log("1. Naik Satu");
function kuadrat() {
    var start = 1;
    while (start <= 15) {
        var nilai = start * start;
        console.log(nilai)
        start++;
    }
}
kuadrat();
console.log("2. kuadrat");
